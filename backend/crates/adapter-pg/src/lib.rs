//! PostgreSQL adapter ‚Äî Zurfur's app-private data boundary.
//!
//! This crate implements the domain port traits (see [`domain::ports`]) against
//! PostgreSQL: the store for app-owned rows keyed by UUIDv7, where the rows are
//! ours to shape and mutate transactionally. It is the private counterpart to
//! `adapter-atproto`, which owns the public boundary (user-owned records on a
//! PDS). See DESIGN/"Domains and Applications" for the split.
//!
//! Per the dependency rule, everything here depends on `domain` and never the
//! reverse; the `api` crate is the composition root that decides this adapter is
//! the one wired in. Each `Pg*` type holds a cloned [`PgPool`] (an `Arc` handle,
//! cheap to clone) and is constructed from one via `new`.
//!
//! # SQL lives in `queries/`, not in Rust
//!
//! Every statement is its own file under `queries/<namespace>/<name>.sql`. The
//! committed [`queries`] module is **generated** from that tree by the
//! `query-codegen` crate (`just gen-queries`), which describes each statement
//! against a live, migrated database and emits typed functions ‚Äî bind arity and
//! types are compile-checked at every call site ‚Äî plus `FromRow` row structs
//! read from the real statement metadata. The `sqlx::query!` macros stay
//! retired (the workspace enables sqlx's `derive` feature, deliberately not
//! `macros`). The `codegen_current` test regenerates in CI and diffs, so stale
//! generated code fails loudly; the behavior suites exercise every statement
//! end-to-end.
//!
//! # Layout
//!
//! Writes go through the compile-enforced Unit of Work (DD `24150017`): the
//! [`PgDatabase`] factory vends a [`PgUnitOfWork`] that owns the transaction, and
//! the per-aggregate *write* views are reachable only on it. The read stores keep
//! the pool and stay non-transactional.
//!
//! - [`PgDatabase`] / [`PgUnitOfWork`] ‚Äî the write factory + transaction handle
//!   ([`domain::ports::Database`] / [`domain::ports::UnitOfWork`]).
//! - [`PgAccountStore`] ‚Äî account/membership reads ([`domain::ports::AccountStore`]);
//!   writes via [`PgAccountWrites`] ([`domain::ports::AccountWrites`]).
//! - [`PgCommissionStore`] ‚Äî commission reads incl. the participant predicate
//!   ([`domain::ports::CommissionStore`]); writes via [`PgCommissionWrites`].
//! - [`PgChangelogStore`] ‚Äî the ordered commission-changelog read
//!   ([`domain::ports::ChangelogStore`]); the append is a [`PgUnitOfWork`] view
//!   ([`PgChangelogWrites`], [`domain::ports::ChangelogWrites`]).
//! - [`PgUserStore`] ‚Äî recognized-visitor reads ([`domain::ports::UserStore`]);
//!   recognition via [`PgUserWrites`] ([`domain::ports::UserWrites`]).
//! - [`PgProfileCache`] ‚Äî read-through profile cache ([`domain::ports::ProfileCache`]);
//!   its best-effort `put` is pool-backed, a documented exception to the Unit of
//!   Work (a cache fill on the read path has no transactional invariant).
//! - [`PgSessionStore`] ‚Äî durable tower-sessions backing store.
//!
//! Pool lifecycle and the migrations embedded from `migrations/` are owned by the
//! free functions below; the binary calls them at boot.

